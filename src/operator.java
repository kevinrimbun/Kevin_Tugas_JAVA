public class operator {
    public static void main(String[] args) {
        int hitung = 10+10;
        System.out.println("hasil dari 10 + 10 adalah " + hitung);

        hitung = hitung - 5;
        System.out.println("kalau dikurang dengan 5 hasilnya adalah " + hitung);

        hitung += 3;
        System.out.println(hitung);

        hitung *= 2;
        System.out.println(hitung);

        hitung -= 4;
        System.out.println(hitung);

        hitung /= 2;
        System.out.println(hitung);
    }
}
