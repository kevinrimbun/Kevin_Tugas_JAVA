public class variable {
    public static void main(String[] args) {
        // Varible var
        var firstName = "Raffi";
        var lastName = "Ahmad";
        var fullName = firstName + " " + lastName;
        System.out.println(fullName);

        // Variable final var
        final var nama = "Ade Rai"; // final var tidak bisa diubah lagi karena sudah final
        System.out.println(nama);

    }
}
