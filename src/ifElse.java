public class ifElse {
    public static void main(String[] args) {
        // if else

        int a = 10;
        int b = 12;

        if (a == 10) {
            if (b == 12){
                System.out.println("a = 10 dan b = 12");
            } else {
                System.out.println(" a bukan 10 dan b bukan 12");
            }
        } else {
            System.out.println("Nilainya salah besar");
        }
        System.out.println("===================");
    }
}
